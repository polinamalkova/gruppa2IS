Вы можете гораздо больше, чем вам кажется.

Вы можете выбирать.

Вы можете выбирать, стоит ли вам сейчас объясняться: «Я не такой, как ты подумал… Я не это имел в виду». Или замкнуться и отдалиться. Или выбрать не проигнорировать себя.

«Что ты имеешь в виду?», «Почему ты считаешь, что я так думаю?», «Ты попадал в подобную ситуацию?» – одним-двумя уточняющими вопросами вы способны направить контакт с человеком в другое русло, где может случиться встреча. Где есть взаимопонимание, интерес, спонтанность, поддержка, живое общение. Где может случиться партнерство, начаться дружба или вспыхнуть любовь.

