Я слышу подобные истории от клиентов на консультациях каждый день. И в своей жизни сталкиваюсь часто. А вам знакомо такое «меню»:

● оценка личности человека по мимике;

● навязанный личный опыт;

● «винегрет» из советов;

● допущения и догмы;

● ярлыки;

● подсвечивание «недостатка»;

● установки и фантазии;

● мнение, основанное на чужом, не прожитом лично опыте;

● взгляд со стороны;

● безосновательные предсказания;

● демонстрация собственных компетенций?

В этом списке – все что угодно, кроме заинтересованности другим человеком.

И хочется кричать: «Умоляю, спроси меня про меня!»


Если в контакте между двумя людьми нет искренней заинтересованности, не остается и шанса на взаимопонимание и поддержку. «Употреблять» такое «блюдо» можно, но «переваривается» оно плохо, хочется «отодвинуть от себя тарелку»: прикрыться формальностями, перевести разговор на другую тему, а то и вовсе отмолчаться.

Не задавать вопросы, не прояснять, не интересоваться. Кажется, будто это встроенная в людей функция.

Почему так?

Контакт с человеком – одно из самых волнительных событий для нашей психики.

Приближение или прикосновение к границе другого всегда сопряжено с ощущением новизны и неизвестности.

Новизна и неизвестность сопровождаются ощущением опасности, отсутствия контроля: «Ты для меня новый, непонятный. Твой образ жизни отличается от моего. Я не знаю, чего ожидать от тебя. С чем я столкнусь, когда подойду ближе? Как подступиться к тебе? Я не могу прогнозировать твои реакции на мои проявления и не понимаю, каким могу быть рядом с тобой. Я не готов к такому количеству нового сразу. Я не чувствую себя в безопасности, я напуган. Поэтому мой способ сближения – это измерять тебя знакомым мне мерилом и накладывать свои проекции».

Или: «Я хорошо тебя знаю! Но сейчас ты поступаешь непонятным для меня образом… Проживаешь свою ситуацию незнакомым мне способом. Я привык, что люди поступают иначе. Я не готов впустить эту новизну в себя. Ведь тогда мне придется что-то с этим делать: пытаться понять, крушить установки, обращаться к толерантности. А это для меня некомфортно. Поэтому я настораживаюсь. И для того, чтобы почувствовать себя в безопасности, я буду складывать твою инаковость на понятную мне "полочку" теми способами, которые мне доступны». Эти способы вы уже видели в «меню» выше.

Вот и получается, что безопаснее оставаться в контакте со своими представлениями о другом человеке, вместо того чтобы контактировать с ним напрямую.

Зачем, например, доказывать свою правоту, спорить, переубеждать, сравнивать? Чтобы почувствовать себя умным, важным, значимым, опытным? И это тоже. Но эта потребность на поверхности, как пенка капучино. А если посмотреть вглубь, задаться вопросом: «Что будет, если я почувствую себя умным, значимым?..» Ответ: «Так я почувствую себя спокойнее и увереннее. Я буду в безопасности». Если в своем представлении мы делаем другого человека похожим на себя, он становится, как нам кажется, более понятным, а значит, более безопасным.

Взволнованность, легкое или интенсивное нервное возбуждение при общении с другим человеком – процесс естественный, сопряженный с работой головного мозга, вегетативной нервной системы. Эта движущая энергия необходима нам для каждого действия. Даже для того, чтобы утром засунуть ноги в тапочки, дойти до кухни и включить чайник, вам нужна капелька внутреннего возбуждения. А для того, чтобы, например, попросить у начальника прибавку к зарплате, этой энергии понадобится целое озерцо.

Волнение – толковый внутренний механизм, который помогает жить.

Не волноваться при контакте с другим человеком не получится. Но проявиться это может по-разному – в зависимости от привычной (часто неосознаваемой) реакции. Можно зажаться в смущении, отмолчаться, отодвинуться. Или идти за волнением, ярко предъявляя себя, фонтанируя своими проекциями и фантазиями о собеседнике. Не плохая и не хорошая, одна и другая реакция – это способ присутствовать рядом с человеком, а не взаимодействовать. И тогда вы как будто остаетесь в своем внутреннем лесу нахаживать круги по выученным тропинкам. Но там нет людей. Нет контакта, новизны и той ценности, которую можно приобрести от искреннего взаимообмена.

Парадокс в том, что почувствовать себя безопасно возможно, только находясь в прямом контакте, задавая вопросы.

Тогда становится яснее.

И часто этой возможности достаточно для того, чтобы снизить зашкаливающее волнение.

Иногда мне звонят люди, которые изначально ничего не знают про меня, кроме того что я «психолог». Они не интересовались информацией на сайте, не читали мою книгу и соцсети. Записал человек номер телефона и ищет опору, ему нужно срочно «приложить» специалиста к свежей ране, чтобы обезболить. Это нормально.

Звонит женщина:

– Здравствуйте! Как можно попасть на консультацию? Сколько стоит? Сколько длится сеанс?

Я отвечаю, даю краткую информацию.

– Это вы ведете прием?

– Да.

Пауза.

– М-м-м… Такой голос молодой… Ну, не знаю, перезвоню.

Иногда напрямую заявляют:

– У вас голос такой… Вы слишком молодая. Поищу более опытного специалиста! До свидания!

Не так давно я встретила свою сорок седьмую весну, а в профессии уже двадцать лет. Природа вложила в мою «базовую комплектацию» звонкий молодой голос. С годами он не меняется. «Ношу» с удовольствием.

Несостоявшийся клиент не задал мне ни одного вопроса про меня, погрузившись в свои проекции: «Молодой голос может быть только у молодой девушки. Следовательно, у нее мало опыта в профессии. Следовательно, она не может быть хорошим специалистом. Вывод: не поможет. Отказ».

И где здесь про меня? Наш контакт не произошел. Это не хорошо и не плохо. Это факт.

Однажды я поддалась любопытству и отчаянно пустилась в эксперимент. Терять-то все равно нечего! Было волнительно «порезаться» о возможные агрессию и отвержение, но я поинтересовалась у дамы на том конце провода, которая уже была готова обесценить меня:

– Молодой голос? А как эта данность характеризует меня как профессионала? Сколько мне лет, по вашему впечатлению? Я обращаю внимание, что вы не поинтересовались.

Дама хмыкнула:

– Спрашивать у женщины про возраст неприлично!

– А вы попробуйте. Вдруг мой ответ что-то изменит. А может, конечно, и нет, – не отставала я.

Видимо, в ту минуту экспериментаторский азарт передался даме, и она рискнула:

– Интересная вы такая. Ну ладно… И сколько же вам лет?

Оказалось, что я на шесть лет старше, что я – «я никогда бы вам столько не дала». В результате дама стала моей клиенткой и у нас получилась ценная терапевтическая работа на год.

